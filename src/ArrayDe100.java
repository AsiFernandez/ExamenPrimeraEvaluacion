
public class ArrayDe100 {

	public static void main(String[] args) {
		
		int array[] = new int[100];
		int contador = 2;
		
		for(int i =0; i< array.length; i++) {
			array[i] = contador;
			contador ++;
		}
		
		
		for(int i =0; i< array.length; i++) {
			System.out.println(array[i]);
		}
		
		
		
		
		
		
	}

}





